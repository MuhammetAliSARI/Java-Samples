package horonaga.HesapMakinesi;

public class HesapMakinesi {
    public int Topla(int sayi1, int sayi2){     // Toplama işlemi
        return sayi1 + sayi2;
    }
    public int Cikar(int sayi1, int sayi2) {    // Çıkarma işlemi
        return sayi1 - sayi2;
    }
    public int Carp(int sayi1, int sayi2) {     // Çarpma işlemi
        return sayi1 * sayi2;
    }
    public int Bol(int sayi1, int sayi2) {      // Bölme işlemi
        return sayi1 / sayi2;
    }

}
