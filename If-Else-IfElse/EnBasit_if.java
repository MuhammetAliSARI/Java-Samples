public class ifEnBasit {
    public static void main(String[] args) {
        boolean varMi=false;
        if (varMi){
            System.out.println("Sayı mevcuttur.");
        }else{
            System.out.println("Sayı mevcut değildir.");
        }

        // Eğer varMi değerini true girmiş olsaydık, ekrana "Sayı mevcuttur" gelecekti.
    }
}
